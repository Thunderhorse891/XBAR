import { randomUUID } from 'node:crypto';
import { readJsonBody, sendJson } from './http.js';
import { requireWorkspaceAccess } from './supabase-admin.js';
import { getTemplateById, renderTemplate } from './document-templates.js';
import { checkStorageCapacity, getWorkspaceEntitlements, tierIncludesPlan } from './entitlements.js';
import { loadHorseContext } from './horse-context.js';
import { createSectionedPdf } from './pdf.js';
import { recordAuditEvent } from './audit.js';
import { enforceRateLimit } from './rate-limit.js';
import { applyCors } from './cors.js';

const DOCUMENT_BUCKET =
  process.env.SUPABASE_DOCUMENT_BUCKET || process.env.VITE_SUPABASE_DOCUMENT_BUCKET || 'horse-documents';
const SIGNED_URL_TTL_SECONDS = 3600;

const RATE_LIMIT = { bucket: 'documents-template', limit: 20, windowSeconds: 60 };

export default async function handler(req, res) {
  if (!applyCors(req, res)) {
    return;
  }

  if (req.method !== 'POST') {
    return sendJson(res, 405, { ok: false, message: 'Method not allowed.' });
  }

  if (!(await enforceRateLimit(req, res, RATE_LIMIT))) {
    return;
  }

  const accessToken = req.headers.authorization?.replace(/^Bearer\s+/i, '').trim() || '';
  let body;
  try {
    body = await readJsonBody(req);
  } catch {
    return sendJson(res, 400, { ok: false, message: 'Request body must be valid JSON.' });
  }

  const workspaceId = typeof body.workspaceId === 'string' ? body.workspaceId : '';
  const templateId = typeof body.templateId === 'string' ? body.templateId : '';
  const horseId = typeof body.horseId === 'string' ? body.horseId : '';
  const output = body.output === 'html' ? 'html' : 'pdf';
  if (!workspaceId || !templateId || !horseId) {
    return sendJson(res, 400, { ok: false, message: 'workspaceId, templateId, and horseId are required.' });
  }

  const access = await requireWorkspaceAccess(accessToken, workspaceId);
  if (!access.ok) {
    return sendJson(res, access.status, { ok: false, message: access.message });
  }
  const { supabase, user } = access;

  const template = getTemplateById(templateId);
  if (!template) {
    return sendJson(res, 404, { ok: false, message: `Unknown template: ${templateId}.` });
  }

  // Tier enforcement: the catalog row in document_templates is authoritative
  // when present so deployments can re-tier templates without a code change.
  const { data: templateRow } = await supabase
    .from('document_templates')
    .select('minimum_plan, template_content, name')
    .eq('id', templateId)
    .maybeSingle();
  const minimumPlan = templateRow?.minimum_plan || template.minimumPlan;

  const entitlements = await getWorkspaceEntitlements(supabase, workspaceId);
  if (!tierIncludesPlan(entitlements.effectiveTier, minimumPlan)) {
    return sendJson(res, 403, {
      ok: false,
      code: 'tier_required',
      message: `The "${template.name}" template requires the ${minimumPlan} plan (current effective plan: ${entitlements.effectiveTier}).`,
      requiredPlan: minimumPlan,
      currentPlan: entitlements.effectiveTier,
      billingState: entitlements.billingState,
    });
  }

  try {
    const loaded = await loadHorseContext(supabase, workspaceId, horseId);
    if (!loaded.horse) {
      return sendJson(res, 404, { ok: false, message: `Horse ${horseId} not found in this workspace.` });
    }

    const context = { ...loaded.context };
    if (templateId === 'sales-packet') {
      context.packet = {
        documentList:
          loaded.documents.map((doc) => `${doc.document_type}: ${doc.title}`).join('; ') || 'No documents attached yet',
      };
    }
    // Caller-supplied placeholder values ({"buyer.name": "...", "sale.price": "..."}).
    for (const [key, value] of Object.entries(body.fields || {})) {
      if (typeof value !== 'string') continue;
      const parts = key.split('.');
      let target = context;
      for (const part of parts.slice(0, -1)) {
        target[part] = target[part] && typeof target[part] === 'object' ? target[part] : {};
        target = target[part];
      }
      target[parts.at(-1)] = value;
    }

    const rendered = renderTemplate({ template, overrideContent: templateRow?.template_content || '', context });
    const generatedAt = new Date().toISOString();
    const fileBase = `${template.id}-${loaded.horse.name.replace(/[^a-zA-Z0-9-]+/g, '-').toLowerCase()}-${generatedAt.slice(0, 10)}`;

    if (output === 'html') {
      return sendJson(res, 200, {
        ok: true,
        templateId,
        title: rendered.title,
        fileName: `${fileBase}.html`,
        html: rendered.html,
        missingFields: rendered.missingFields,
      });
    }

    const pdfBytes = await createSectionedPdf({
      title: rendered.title,
      sections: rendered.sections,
      footer: `Generated by XBAR for ${context.workspace.businessName || 'workspace'} on ${generatedAt.slice(0, 10)}`,
    });
    const pdfSizeBytes = pdfBytes.byteLength;

    const storage = await checkStorageCapacity(supabase, workspaceId, pdfSizeBytes, entitlements.limits);
    if (!storage.ok) {
      return sendJson(res, 403, {
        ok: false,
        code: 'storage_limit_reached',
        message: storage.message,
        currentPlan: entitlements.effectiveTier,
        billingState: entitlements.billingState,
      });
    }

    const documentId = `doc-${randomUUID()}`;
    const storagePath = `${user.id}/${workspaceId}/${documentId}/${fileBase}.pdf`;
    const { error: uploadError } = await supabase.storage
      .from(DOCUMENT_BUCKET)
      .upload(storagePath, Buffer.from(pdfBytes), { contentType: 'application/pdf', upsert: true });
    if (uploadError) {
      return sendJson(res, 502, { ok: false, message: `Failed to store generated PDF: ${uploadError.message}` });
    }

    const { error: recordError } = await supabase.from('documents').upsert({
      workspace_id: workspaceId,
      document_id: documentId,
      horse_id: horseId,
      title: rendered.title,
      document_type: templateId === 'bill-of-sale' ? 'Bill of Sale' : 'Ownership Memo',
      source: 'Sales Packet',
      state: 'Ready',
      confidence: 1,
      original_filename: `${fileBase}.pdf`,
      storage_path: storagePath,
      mime_type: 'application/pdf',
      size_bytes: pdfSizeBytes,
      extracted_data: { templateId, generatedAt },
      needs_review: false,
      uploaded_by_user_id: user.id,
      payload: { generated: true, templateId, missingFields: rendered.missingFields },
    });
    if (recordError) {
      // The row was rejected after the object was written (e.g. the storage
      // trigger firing on a concurrent request that slipped past the preflight).
      // Delete the orphan and surface the failure rather than signing a URL to a
      // document that was never recorded.
      await supabase.storage.from(DOCUMENT_BUCKET).remove([storagePath]);
      const overLimit = /storage limit/i.test(recordError.message);
      return sendJson(res, overLimit ? 403 : 502, {
        ok: false,
        code: overLimit ? 'storage_limit_reached' : undefined,
        message: overLimit
          ? `This document would exceed the plan's ${entitlements.limits.storageLimitGb} GB storage limit. Upgrade to continue.`
          : `Failed to record generated document: ${recordError.message}`,
      });
    }

    const { data: signed } = await supabase.storage
      .from(DOCUMENT_BUCKET)
      .createSignedUrl(storagePath, SIGNED_URL_TTL_SECONDS);

    await recordAuditEvent(supabase, {
      workspaceId,
      actorUserId: user.id,
      action: 'document.generated_from_template',
      entityType: 'document',
      entityId: documentId,
      metadata: { templateId, horseId, missingFields: rendered.missingFields },
    });

    return sendJson(res, 200, {
      ok: true,
      templateId,
      documentId,
      title: rendered.title,
      fileName: `${fileBase}.pdf`,
      storagePath,
      downloadUrl: signed?.signedUrl || '',
      expiresInSeconds: SIGNED_URL_TTL_SECONDS,
      missingFields: rendered.missingFields,
    });
  } catch (error) {
    return sendJson(res, 500, { ok: false, message: `Template generation failed: ${error.message}` });
  }
}
